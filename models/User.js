const mongoose = require('mongoose');

const UserSchema = new mongoose.Schema({
  googleId: {
    type: String,
    required: true,
    unique: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  name: {
    type: String,
    required: true
  },
  picture: {
    type: String
  }
}, { timestamps: true }); // Auto-manages createdAt and updatedAt

module.exports = mongoose.models.User || mongoose.model('User', UserSchema);
